    private void btnPolaczActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPolaczActionPerformed
        String serverIp = JOptionPane.showInputDialog(this, "Podaj IP serwera:", "localhost");
    
        String username = JOptionPane.showInputDialog(this, "Podaj swoją nazwę użytkownika:");

        if (username == null || username.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Musisz podać nazwę użytkownika!");
            return;
        }

        boolean connected = client.connect(serverIp, 2011, username);

        if (connected) {
            labelCzyUruchomiony.setText("Połączono jako " + username);
            addMessage("[Połączono z serwerem " + serverIp + "]");
        } else {
            labelCzyUruchomiony.setText("Nie udało się połączyć");
        }
    }//GEN-LAST:event_btnPolaczActionPerformed

