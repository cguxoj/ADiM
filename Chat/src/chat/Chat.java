    private void connectToServer() {
        String ip = JOptionPane.showInputDialog(this, "Podaj IP serwera:");
        String login = JOptionPane.showInputDialog(this, "Podaj swój login:");
        if (client.connect(ip, 2011, login)) {
            labelCzyUruchomiony.setText("Połączony jako " + login);
        }
    }

    private void sendMessage() {
        String msg = fieldWiadomosc.getText().trim();
        if (!msg.isEmpty()) {
            client.sendMessage(msg);
            fieldWiadomosc.setText("");
        }
    }

    public void addMessage(String msg) {
        javax.swing.SwingUtilities.invokeLater(() -> {
            areaWiadomosci.append(msg + "\n");
        });
    }

    public void updateUserList(String[] users) {
        javax.swing.SwingUtilities.invokeLater(() -> {
            listUzytkownicy.setListData(users);
        });
    }

    public void updateServerStatus(String status) {
        javax.swing.SwingUtilities.invokeLater(() -> {
            labelCzyUruchomiony.setText(status);
        });
    }

    public void addUser(String username) {
        addMessage("[Dołączył] " + username);
    }

    public void removeUser(String username) {
        addMessage("[Opuścił czat] " + username);
    }

    private void sendPrivateMessage(String targetUser) {
        String message = JOptionPane.showInputDialog(this, "Wpisz wiadomość do " + targetUser);
        if (message != null && !message.trim().isEmpty()) {
            client.sendMessage("/w " + targetUser + " " + message); // Wysyłamy wiadomość prywatną do serwera
        }
    }
    
